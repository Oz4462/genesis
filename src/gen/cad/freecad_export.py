"""FreeCAD-oriented assembly mate export (residual FreeCAD integration gap).

Does **not** require FreeCAD installed. Emits:
  * ``assembly_mates.json`` — portable mate graph (offset/stack_z/align_xy + labels)
  * ``assembly_freecad.py`` — a FreeCAD Python macro that rebuilds the assembly with
    Part Placement offsets when opened in FreeCAD (optional runtime).

Full FreeCAD Assembly workbench / App::Link constraint solver is still an external
step — this closes the "no FreeCAD seam" gap with a real, loadable handoff.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any


def build_mates_document(
    parts: list[dict[str, Any]],
    constraints: list[dict[str, Any]],
    *,
    name: str = "GenesisAssembly",
    run_id: str | None = None,
) -> dict[str, Any]:
    """Structured mate document for FreeCAD / other CAD import.

    ``parts``: [{label, pos: [x,y,z], stl?, bbox_mm?}, ...]
    ``constraints``: [{kind, fixed, moving, offset_mm, gap_mm, note}, ...]
    """
    if not parts:
        raise ValueError("build_mates_document: parts must be non-empty")
    return {
        "schema": "genesis-freecad-mates-v1",
        "name": name,
        "run_id": run_id,
        "parts": parts,
        "constraints": constraints,
        "freecad": {
            "target": "FreeCAD 0.21+ Part workbench macro (assembly_freecad.py)",
            "assembly_wb": "not auto-generated — import macro then optionally convert",
        },
        "gaps": [
            "native .FCStd binary not written (requires FreeCAD app)",
            "Assembly4/A2plus solver constraints not emitted — placements only",
        ],
        "quelle": "gen.cad.freecad_export.build_mates_document",
    }


def freecad_macro_text(doc: dict[str, Any]) -> str:
    """Generate a FreeCAD Python macro that places imported STLs / boxes by position."""
    name = doc.get("name") or "GenesisAssembly"
    parts = doc.get("parts") or []
    lines = [
        "# Auto-generated by GENESIS freecad_export — open in FreeCAD: Macro → Execute",
        "import FreeCAD as App",
        "import FreeCADGui as Gui",
        "import Part",
        "from FreeCAD import Vector, Placement, Rotation",
        "",
        f'doc = App.newDocument("{name}")',
        "",
    ]
    for i, p in enumerate(parts):
        lab = str(p.get("label") or f"Part{i}").replace('"', "")
        pos = p.get("pos") or [0, 0, 0]
        bbox = p.get("bbox_mm") or [20, 20, 5]
        stl = p.get("stl")
        lines.append(f"# --- {lab} ---")
        if stl:
            lines.append("try:")
            lines.append(
                f'    obj = doc.addObject("Mesh::Feature", "{lab}")'
            )
            lines.append("    import Mesh")
            lines.append(f'    obj.Mesh = Mesh.Mesh(r"{stl}")')
            lines.append("except Exception:")
            lines.append(
                f'    obj = doc.addObject("Part::Box", "{lab}")'
            )
            lines.append(f"    obj.Length = {float(bbox[0])}")
            lines.append(f"    obj.Width = {float(bbox[1])}")
            lines.append(f"    obj.Height = {float(bbox[2])}")
        else:
            lines.append(f'obj = doc.addObject("Part::Box", "{lab}")')
            lines.append(f"obj.Length = {float(bbox[0])}")
            lines.append(f"obj.Width = {float(bbox[1])}")
            lines.append(f"obj.Height = {float(bbox[2])}")
        lines.append(
            f"obj.Placement = Placement(Vector({float(pos[0])}, {float(pos[1])}, "
            f"{float(pos[2])}), Rotation())"
        )
        lines.append("")
    lines += [
        "doc.recompute()",
        "try:",
        "    Gui.SendMsgToActiveView('ViewFit')",
        "except Exception:",
        "    pass",
        f'print("GENESIS assembly {name}: {len(parts)} parts placed")',
        "",
    ]
    return "\n".join(lines)


def write_freecad_export(
    pkg_root: Path,
    doc: dict[str, Any],
) -> dict[str, str]:
    """Write mates JSON + FreeCAD macro; return {json, macro} paths."""
    root = Path(pkg_root)
    root.mkdir(parents=True, exist_ok=True)
    jpath = root / "assembly_mates.json"
    mpath = root / "assembly_freecad.py"
    jpath.write_text(json.dumps(doc, indent=2, ensure_ascii=False), encoding="utf-8")
    mpath.write_text(freecad_macro_text(doc), encoding="utf-8")
    return {"json": str(jpath), "macro": str(mpath)}
