"""professional_package — the last pipeline stage: a real, honest deliverable package.

Takes whatever a GENESIS run actually produced (a plain ``dict``) and writes a professional deliverable:
a Markdown master document + a styled, **printable** HTML (open it and Ctrl-P → PDF). A real PDF file is
produced only if a PDF backend (weasyprint) is installed; otherwise it is honestly SKIPPED with the reason.

ANTI-FABRICATION CONTRACT (this is GENESIS — STATUS.md §1):
  * It aggregates ONLY data that is present in the result. A missing gate result, goldset score, or claim
    list is rendered as "not available / not measured / not run" — NEVER as a fabricated default (the pasted
    draft used ``score = ... or 97`` and ``gates = ... or {all ✅}``; both are removed here).
  * It WRITES files and reports exactly what it wrote and what it skipped — no "PDF FERTIG / COMPLETE" banner
    over an empty path (the ``breakthrough_bridge`` anti-pattern).
  * It is domain-aware: humanoid research is included only when the project is humanoid (or research is
    explicitly supplied) — it is not force-injected into, say, a cooling deliverable.
"""

from __future__ import annotations

import html
import json
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

try:
    from ..visualization.robust_renderer import RobustVisualizer
except Exception:
    RobustVisualizer = None  # graceful if not present

# Product surface: 2-D drawing export is part of the deliverable stack (optional runtime).
try:
    from ..export import drawing as _export_drawing  # noqa: F401
except Exception:  # pragma: no cover - optional build123d venv
    _export_drawing = None

_MISSING = "_(not available in this result)_"


def _as_int_or_none(v: Any) -> int | float | None:
    return v if isinstance(v, (int, float)) and not isinstance(v, bool) else None


def _gate_lines(gates: Any) -> tuple[list[str], bool]:
    """Render REAL gate verdicts. Returns (lines, any_present). Never fabricates an all-green default."""
    if not isinstance(gates, dict) or not gates:
        return ([], False)
    lines = []
    for name, val in gates.items():
        if isinstance(val, bool):
            mark = "✅ PASS" if val else "❌ FAIL"
        elif hasattr(val, "passed"):
            mark = "✅ PASS" if val.passed else "❌ FAIL"
        else:
            mark = str(val)
        lines.append(f"- **{name}**: {mark}")
    return (lines, True)


def _claim_lines(claims: Any) -> tuple[list[str], int]:
    if not isinstance(claims, (list, tuple)) or not claims:
        return ([], 0)
    lines = []
    for c in claims[:50]:
        text = getattr(c, "text", None) or (c.get("text") if isinstance(c, dict) else None) or str(c)
        status = getattr(c, "status", None)
        status = getattr(status, "value", status) if status is not None else None
        suffix = f"  _[{status}]_" if status else ""
        lines.append(f"- {text}{suffix}")
    return (lines, len(claims))


@dataclass
class ProfessionalPackage:
    """Aggregate a GENESIS result dict and forge an honest deliverable package."""

    genesis_result: dict
    included: list[str] = field(default_factory=list)
    missing: list[str] = field(default_factory=list)

    def _section(self, title: str, body: str | None) -> str:
        if body and body.strip() and body.strip() != _MISSING:
            self.included.append(title)
        else:
            self.missing.append(title)
        return f"## {title}\n\n{body or _MISSING}\n"

    def _build_markdown(self) -> str:
        r = self.genesis_result
        name = str(r.get("name") or r.get("idea") or "GENESIS Project")
        idea = str(r.get("idea") or "")
        score = _as_int_or_none(r.get("goldset_score"))
        gate_lines, gates_present = _gate_lines(r.get("gates"))
        claim_lines, n_claims = _claim_lines(r.get("claims"))
        physics = r.get("physics_verified")
        spec = r.get("spec")
        bundle = r.get("bundle") or r.get("manifest")
        gaps = r.get("gaps") or []

        parts: list[str] = []
        parts.append(f"# {name} — GENESIS Deliverable\n")
        parts.append(f"_Generated by GENESIS finalizer. Idea: {idea or name}._\n")
        parts.append(
            "> Integrity note: every section below is built from what this run actually produced. "
            "Anything not produced is marked **not available** — not invented.\n"
        )

        # Executive summary — only honest, present facts.
        es = []
        es.append(f"- **Physics gate (δ):** {'✅ verified' if physics else ('❌ not verified' if physics is not None else _MISSING)}")
        es.append(f"- **Verified claims:** {n_claims if n_claims else _MISSING}")
        es.append(
            f"- **Goldset anti-hallucination score:** {score if score is not None else 'not measured — run `genesis --mode goldset`'}"
        )
        es.append(f"- **Honest gaps carried:** {len(gaps) if gaps else 0}")
        parts.append(self._section("Executive Summary", "\n".join(es)))

        # GENESIS gate trace — REAL verdicts only.
        parts.append(self._section(
            "GENESIS Gate Trace",
            ("\n".join(gate_lines) if gates_present
             else "No gate verdicts were captured in this result (the producing mode did not attach them).")
            + ("" if gates_present else f"\n\n{_MISSING}")
        ))

        # Verified claims.
        parts.append(self._section(
            "Verified Claims (sourced)",
            (f"{n_claims} claim(s):\n\n" + "\n".join(claim_lines)) if n_claims else None
        ))

        # Technical specification.
        spec_body = None
        if spec is not None:
            qs = getattr(spec, "quantities", None) or (spec.get("quantities") if isinstance(spec, dict) else None) or []
            rows = []
            for q in qs[:60]:
                qn = getattr(q, "name", None) or (q.get("name") if isinstance(q, dict) else "")
                qv = getattr(q, "value", None) or (q.get("value") if isinstance(q, dict) else "")
                qu = getattr(q, "unit", None) or (q.get("unit") if isinstance(q, dict) else "")
                qm = getattr(q, "measurand", None) or (q.get("measurand") if isinstance(q, dict) else "")
                rows.append(f"| {qn} | {qv} | {qu} | `{qm or ''}` |")
            spec_body = ("| Quantity | Value | Unit | Measurand |\n|---|---|---|---|\n" + "\n".join(rows)) if rows \
                else "Specification present but carries no measurand-tagged quantities."
        parts.append(self._section("Technical Specification", spec_body))

        # Assembly / BOM — from the real bundle if present.
        bom_body = None
        if bundle is not None:
            outp = getattr(bundle, "out_dir", None) or (bundle.get("out_dir") if isinstance(bundle, dict) else None)
            files = getattr(bundle, "files", None) or (bundle.get("files") if isinstance(bundle, dict) else None)
            bom_body = (
                f"Realization bundle: `{outp or 'n/a'}`\n\n"
                "See the bundle directory for the build manual (BAUANLEITUNG.md), BOM (bom.json) and any STL/SCAD."
                + (f"\n\nFiles: {files}" if files else "")
            )
        parts.append(self._section("Assembly & Bill of Materials", bom_body))

        # Visuals — honest about what is NOT rendered.
        parts.append(self._section(
            "Visuals",
            "No images are rendered by the finalizer (no CAD/sim image renderer is wired). "
            "If a bundle is present, its STL/SCAD are the geometry of record; render them in your CAD viewer."
        ))

        # Research — ONLY when humanoid / explicitly supplied (domain-aware, not force-injected).
        research = r.get("research") or r.get("humanoid_research")
        is_humanoid = any(k in (name + " " + idea).lower() for k in ("humanoid", "aethon", "roboter", "robot"))
        research_body = None
        if research and (is_humanoid or r.get("force_research")):
            research_body = str(research)[:8000]
        elif research and not is_humanoid:
            research_body = "_(research report supplied but omitted — not relevant to this (non-humanoid) project)_"
        parts.append(self._section("Research Insights", research_body))

        # Honest gaps.
        parts.append(self._section(
            "Honest Gaps & Limits",
            ("\n".join(f"- {g}" for g in gaps)) if gaps else "No gaps were attached to this result."
        ))

        return "\n".join(parts)

    def _build_html(self, markdown_text: str) -> str:
        """A self-contained, printable HTML rendering (no external deps). Minimal MD → HTML for headings,
        lists and tables; everything else is escaped paragraph text. Open + Ctrl-P for a PDF."""
        body_lines: list[str] = []
        in_table = False
        in_list = False

        def close_list():
            nonlocal in_list
            if in_list:
                body_lines.append("</ul>")
                in_list = False

        def close_table():
            nonlocal in_table
            if in_table:
                body_lines.append("</table>")
                in_table = False

        for raw in markdown_text.splitlines():
            line = raw.rstrip()
            if line.startswith("| ") and "|" in line[2:]:
                cells = [c.strip() for c in line.strip().strip("|").split("|")]
                if set("".join(cells)) <= set("-: "):  # separator row
                    continue
                close_list()
                if not in_table:
                    body_lines.append("<table>")
                    in_table = True
                tag = "td"
                body_lines.append("<tr>" + "".join(f"<{tag}>{html.escape(c)}</{tag}>" for c in cells) + "</tr>")
                continue
            close_table()
            if line.startswith("# "):
                close_list()
                body_lines.append(f"<h1>{html.escape(line[2:])}</h1>")
            elif line.startswith("## "):
                close_list()
                body_lines.append(f"<h2>{html.escape(line[3:])}</h2>")
            elif line.startswith("- "):
                if not in_list:
                    body_lines.append("<ul>")
                    in_list = True
                body_lines.append(f"<li>{html.escape(line[2:])}</li>")
            elif line.startswith("> "):
                close_list()
                body_lines.append(f"<blockquote>{html.escape(line[2:])}</blockquote>")
            elif line.strip():
                close_list()
                body_lines.append(f"<p>{html.escape(line)}</p>")
        close_list()
        close_table()

        css = (
            "body{font-family:-apple-system,Segoe UI,Roboto,sans-serif;max-width:820px;margin:2rem auto;"
            "padding:0 1.5rem;color:#1f2937;line-height:1.5}h1{color:#0f766e;border-bottom:3px solid #10b981;"
            "padding-bottom:.3rem}h2{color:#0f766e;margin-top:2rem}table{border-collapse:collapse;width:100%;"
            "margin:1rem 0}td{border:1px solid #d1d5db;padding:.4rem .6rem;font-size:.9rem}"
            "blockquote{background:#f0fdfa;border-left:4px solid #10b981;margin:1rem 0;padding:.6rem 1rem;color:#374151}"
            "code{background:#f3f4f6;padding:.1rem .3rem;border-radius:3px;font-size:.85rem}"
            "ul{margin:.5rem 0}@media print{body{margin:0}}"
        )
        return f"<!doctype html><html><head><meta charset='utf-8'><style>{css}</style></head><body>" \
               + "\n".join(body_lines) + "</body></html>"

    def _try_pdf(self, html_text: str, out_dir: Path) -> tuple[str | None, str | None]:
        """Real PDF only if a backend is installed; else honest skip with a reason."""
        try:
            import weasyprint  # type: ignore
        except Exception:
            return (None, "PDF backend not installed (pip install weasyprint) — open DELIVERABLE.html and print to PDF.")
        try:
            pdf_path = out_dir / "DELIVERABLE.pdf"
            weasyprint.HTML(string=html_text).write_pdf(str(pdf_path))
            return (str(pdf_path), None)
        except Exception as e:  # noqa: BLE001 — surfaced, not hidden
            return (None, f"weasyprint present but failed: {type(e).__name__}: {e}")

    def forge(self, out_dir: str | Path | None = None) -> dict:
        r = self.genesis_result
        name = str(r.get("name") or r.get("idea") or "Project")
        slug = re.sub(r"[^\w.-]+", "_", name).strip("_")[:64] or "Project"
        out = Path(out_dir) if out_dir else Path("out") / f"{slug}_GENESIS_deliverable"
        out.mkdir(parents=True, exist_ok=True)

        md = self._build_markdown()
        md_path = out / "DELIVERABLE.md"
        md_path.write_text(md, encoding="utf-8")

        html_text = self._build_html(md)
        html_path = out / "DELIVERABLE.html"
        html_path.write_text(html_text, encoding="utf-8")

        pdf_path, pdf_skipped = self._try_pdf(html_text, out)

        result = {
            "out_dir": str(out),
            "markdown": str(md_path),
            "html": str(html_path),
            "pdf": pdf_path,
            "pdf_skipped": pdf_skipped,
            "included": self.included,
            "missing": self.missing,
        }
        (out / "MANIFEST.json").write_text(json.dumps(result, indent=2), encoding="utf-8")

        # Honest summary — ASCII only (emoji breaks pytest capture on some CI locales).
        print(f"[package] Deliverable written to {out}/")
        print("   * DELIVERABLE.md + DELIVERABLE.html (printable -> PDF) + MANIFEST.json")
        if pdf_path:
            print("   * DELIVERABLE.pdf")
        else:
            print(f"   * PDF: skipped — {pdf_skipped}")
        print(
            f"   * sections with real content: {len(self.included)} | not available: {len(self.missing)}"
        )

        # Visuals: SYNCHRONOUS + quiet under pytest (daemon threads race capture teardown
        # and were observed as UnicodeDecodeError storms on CI 3.12).
        if RobustVisualizer is not None and "pytest" not in sys.modules:
            try:
                vis = RobustVisualizer()
                vis.auto_integrate(r)
            except Exception as e:
                print(f"[package] Visuals skipped: {e}")

        return result


def finalize_pipeline(genesis_result: dict, out_dir: str | Path | None = None) -> dict:
    """Package a GENESIS run's results into the deliverable. Call at the end of a mode (opt-in)."""
    return ProfessionalPackage(genesis_result=genesis_result).forge(out_dir)
